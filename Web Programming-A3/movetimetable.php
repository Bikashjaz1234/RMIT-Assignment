function currency_format(n_money) {
	//no cents
	if (n_money.indexOf(".") == -1) {
		if (n_money.length == 0) {
			return " 0.00";
		}
		if (n_money.length == 1) {
			return " " + n_money + ".00";
		} else {
			return n_money + ".00";
		}
	} else {
		if ((n_money.substring(0, n_money.indexOf("."))).length == 1) {
			n_money = " " + n_money;
		}
		if ((n_money.substring(n_money.indexOf(".") + 1, n_money.length)).length == 0) {
			n_money = n_money + "00"
		}
		if ((n_money.substring(n_money.indexOf(".") + 1, n_money.length)).length == 1) {
			n_money = n_money + "0"
		}
		if ((n_money.substring(n_money.indexOf(".") + 1, n_money.length)).length > 2) {
			var secondigit = parseInt(n_money.substring((n_money.indexOf(".") + 2), (n_money.indexOf(".") + 3)));
			var thirsdigit = parseInt(n_money.substring((n_money.indexOf(".") + 3), (n_money.indexOf(".") + 4)));
			if (thirsdigit >= 5) {
				n_money = n_money.substring(0, n_money.indexOf(".") + 2) + (secondigit + 1);
			} else {
				n_money = n_money.substring(0, n_money.indexOf(".") + 3)
			}
		}
		return n_money;
	}
}
